#3.Assignment operators
'''
var=var op val
var op=var
'''
e=100
e+=100
print('e+=100:',e)#e+=100:200
e-=100
print('e-=100:',e)#e-=100:100
e*=100
print('e*=100:',e)#e*=100:10000
e//=100
print('e//=100:',e)#e//=100:100
e/=100
print('e**=10:',e)#e**10:10000000000000
e**=100
print('e%=10:',e)#e%=10:1
e%=100
print('e/=5:',e)#e/=5:0.2
